valor_1 = 'valor_1'
valor_2 = 'valor_2'
valor_7 = 'valor_7'
# Los diccionarios nos permiten utilizar una clave para declarar y acceder a un valor
mi_diccionario = {'clave_1': valor_1, 'clave_2': valor_2, 'clave_7': valor_7}

print(mi_diccionario['clave_2'])  # Imprime valor_2

# Se permite borrar cualquier entrada,asi como modificar valores

del (mi_diccionario['clave_2'])
print(mi_diccionario)

mi_diccionario['clave_1'] = 'Nuevo_valor'
print(mi_diccionario)
