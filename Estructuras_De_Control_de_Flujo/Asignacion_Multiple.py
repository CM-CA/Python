# Ejemplos de asignacion  multiple

a, b, c = 'string', 15, True
print(a)
print(b)
print(c)

#
# La asignación múltiple de variables,
# también puede darse utilizando como valores, el contenido de una tupla
#

mi_tupla = ('hola mundo', 2018)
texto, anio = mi_tupla

print(texto)
print(anio)
print(mi_tupla)

# Tambien puede darse en listas

mi_lista = ['España', 'Madrid']
pais, capital = mi_lista

print(capital)
print(pais)
